const { model, Schema } = require("mongoose");

const registralSchema = new Schema(
  {
    RegId: {
      type: String,
    },
    email: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
    },
    firstName: {
      type: String,
      required: true,
    },
    lastName: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

module.exports = model("Registral", registralSchema);
